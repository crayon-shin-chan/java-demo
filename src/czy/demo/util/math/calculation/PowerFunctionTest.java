package czy.demo.util.math.calculation;

public class PowerFunctionTest {

    public static void main(String[] args){

        System.out.println("2的开方为："+Math.sqrt(2));
        System.out.println("3的开方为："+Math.sqrt(3));
        System.out.println("5的开方为："+Math.sqrt(5));

        System.out.println("2的0.5次方为："+Math.pow(2,0.5));
        System.out.println("2的1.5次方为："+Math.pow(2,1.5));
        System.out.println("2的10次方为："+Math.pow(2,10));
        System.out.println("3的0.5次方为："+Math.pow(3,0.5));

    }

}
