package czy.demo.util.math.calculation;

public class OtherTest {

    public static void main(String[] args){

        System.out.println("-1的绝对值为："+Math.abs(-1));
        System.out.println("-1.1的绝对值为："+Math.abs(-1.1));
        System.out.println("1的绝对值为："+Math.abs(1));
        System.out.println("1.1的绝对值为："+Math.abs(1.1));
        System.out.println();

        System.out.println("获取1与5的最大值："+Math.max(1,5));
        System.out.println("获取1与5的最小值："+Math.min(1,5));
        System.out.println();
    }

}
